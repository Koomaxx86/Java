package Day01;

public class Ex05_Variable {
	public static void main(String[] args) {
		// 변수 선언
		// - 자료형 변수명;
		
		// 변수 선언 및 초기화
		// - 자료형 변수명 = 값;
		int x;
		int y;
		
		// = (대입연산자)
		// A = B
		x = 10;
		y = 20;
		
		System.out.println("x : " + x);
		System.out.println("y : " + y);
		System.out.println("x + y : " + (x+y));
		System.out.println("(x + y) / 2 = : " + (x+y)/2);
	}
}
